        /// <summary>
        ///微信app支付二次验签
        /// </summary>
        /// <param name="parameter"></param>
        /// <returns></returns>
        public static string MakeAppSign( SortedDictionary<string, object> parameter )
        {
            WxPayData wxPayData = new WxPayData();
            //转url格式
            wxPayData.SetValue(parameter);
            string str = wxPayData.ToUrl();
            str += "&key=" + WxAppPayConfig.KEY;
            //MD5加密
            var md5 = MD5.Create();
            var bs = md5.ComputeHash(Encoding.UTF8.GetBytes(str));
            var sb = new StringBuilder();
            foreach (byte b in bs)
            {
                sb.Append(b.ToString("x2"));
            }
            //所有字符转为大写
            return sb.ToString().ToUpper();
        }
